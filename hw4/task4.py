text = input()
print(f'третій символ рядка {text}: {text[2]}')
print(f'передостанній символ рядка{text}: {text[-2]}')
print(f'перші п’ять символів рядка {text}: {text[:5]}')
print(f'весь рядок {text}, крім двох останніх символів: {text[:-2:]}')
print(f'усі символи з парними індексами рядка {text}: {text[::2]}')
print(f'усі символи з непарними індексами рядка {text}: {text[1::2]}')
print(f'усі символи рядка {text} зворотньому порядку: {text[::-1]}')
print(f'усі символи рядка {text} через один у зворотньому порядку: {text[::-2]}')
print(f'довжина рядка {text} складає {len(text)} символів')